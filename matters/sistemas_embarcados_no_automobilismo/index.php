        <h2>Carros Autônomos</h2>

        <p>Quando o assunto são os tipos de automóveis que existirão no futuro, uma categoria
        que se destaca bastante e deverá ganhar espaço ao longo dos próximos anos, 
        essa que é a dos carros autônomos. Desde a virada do milênio, esse tipo de 
        veículo vem sendo idealizado e aprimorado de forma contínua por várias 
        empresas de tecnologia, como por exemplo: Apple, Audi, Baidu, GM, Samsung, 
        e a Tesla.   
        </p>

        </br> 

        <p>Os carros autônomos foram planejados com o intuito de reduzir quatro
        problemas muito comuns nas estradas e vias urbanas: os acidentes, a 
        poluição sonora, os engarrafamentos e a alta emissão de gases poluentes. 
        </p>

        </br> 

        <p>Com a finalidade de tornar o futuro melhor, as seguintes características
        foram idealizadas.
        </p>
        
        <br/>

        <p>Para que os carros autônomos possam existir, é de suma importância que se tenham
        sensores espalhados por todo o veículo, o que possibilita que o automóvel seja guiado 
        sem o auxílio humano e somente através da inteligência de computadores. Entre as suas 
        competências, os sensores são responsáveis por: identificar pedestres, outros automóveis 
        e qualquer outro obstáculo que possa aparecer nas pistas.  
        </p>

        </br>

        <p>A interação realizada em tempo real entre esses sensores e o computador central do veícul
        é o que permite que as tomadas de decisão sejam feitas de maneira praticamente imediata.
        Nos dias de hoje, a tecnologia já está evoluída ao ponto de que Carros autônomos sejam capazes
        de lidar com semáforos quebrados e ruas fechadas, e até mesmo com o trânsito desviado.
        </p>

        </br>
        
        <p>De forma resumida, a inteligência artificial utilizada para o funcionamento
          dos carros autônomos consegue: 
        </p>

        <br/>

        <ul>
          <li>Analisar a pressão dos pneus;</li>
          <li>Acabar com os “pontos cegos”;</li>
          <li>Utilizar uma rápida resposta de frenagem em possíveis situações de perigo;</li>
          <li>Se ater aos limites de velocidade da estrada;</li>
          <li>Detectar mudanças de faixa;</li>
          <li>Mudar a suspensão do veículo sempre que necessário;</li>
          <li>Realizar as mais diversas manobras sem que ocorram colisões;</li>
        </ul>
        
        </br>

        <p>A tecnologia de direção 100% autônoma ainda está um pouco longe de se tornar uma realidade 
          para a maioria dos donos de carro. Mas o avanço tecnológico está chegando mesmo para os segmentos 
          de entrada do mercado, com carros compactos passando a incorporar tecnologias de assistência que
          até há pouco tempo eram impensáveis. E por mais que se imagine que seja algo que começou a muitos
          anos atrás, sua história é bem recente. 
        </p>

        </br> 

        <h2>História dos Carros Autônomos </h2>
      
        <p>O primeiro projeto de carro autônomo como conhecemos hoje foi desenvolvido pelo Google em 2010. 
          De lá para cá, diversas montadoras e grandes empresas como Lyft, Uber, Apple, Samsung decidiram
          entrar nesse mercado, acelerando o desenvolvimento de novas tecnologias que melhoram a performance
          e aumentam a segurança desses veículos.  
        </p>

        <br/>

        <p>
        O Brasil também possui seus protótipos de carros autônomos. Na Faculdade de Engenharia de UFMG já está
        em testes o CADU (Carro Autônomo Desenvolvido), enquanto na USP de São Carlos, os trabalhos com o CaRINA
        (Carro Inteligente para Navegação Autônoma) seguem desde 2011. 
        Segundo a Ernst & Young, a previsão é que em 2025 os carros autônomos representem 4% do total de veículos 
        vendidos no mundo e em 2035 essa previsão sobe para 75%. Porém, sabendo de tudo isso, fica a duvida. Como
        que os carros autônomos funcionam?  
        </p>

        <br/>

        <h2>tecnologia por trás dos carros autônomos</h2>

        <p>Ideia para os carros autônomos é chegar ao ponto em que o motorista seja dispensável, 
        fazendo com que todos os ocupantes apenas aproveitem a viagem. Para isso, a tecnologia
        precisa evoluir a um patamar de segurança confiável. 
        </p>

        <br/>

        <p>Para manter o programa armazenado, micro controladores possuem memória
          não volátil do tipo Flash. Alguns ainda integram uma EEPROM, sendo que é
          usada para manter dados de configuração da aplicação, por exemplo, dados 
          de calibração. Já os dados são carregados em memória volátil do tipo SRAM
          durante a inicialização do programa. A memória pode ser definida como um 
          conjunto de elementos que armazenam informação. Em uma memória semicondutora,
          o elemento básico é chamado célula de memória. A célula de memória é capaz de
          armazenar a informação de 1 bit, isto é, apresenta dois estados estáveis, 1 ou 0.
          Além disso, deve possibilitar que a informação seja definida (gravada)
          e verificada (lida).
        </p>

        <br/>

        <h3>OBD</h3>

        <p>OBD (do inglês On-Board Diagnostic) designa um sistema de autodiagnóstico
        disponível na maioria dos veículos automóveis que circulam atualmente. 
        A conexão ao sistema consiste em um conector padronizado que foi sancionado
        como obrigatório na Europa e nos Estados Unidos para todos os veículos produzidos
        desde 1996, e no Brasil a partir de 2010 com o padrão de segunda geração OBD2 (ou ODBII).
        A medida tem a finalidade de popularizar o serviço de reparo eletrônico,
        reduzindo drasticamente o custo das oficinas, possibilitando o consumidor 
        pagar mais barato por esse gênero de serviço. Além disso, a padronização e 
        abertura dos protocolos de comunicação trouxeram ao mercado equipamentos 
        extremamente baratos possibilitando a compra dos mesmos pelos próprios 
        consumidores para "diagnóstico em casa". Atualmente existem no mercado 
        equipamentos de diagnóstico na ordem de $20 dólares com interface USB e t
        ecnologia Bluetooth para sincronização com um computador ou aparelho celular.  
        </p>

        <br/>

        <h3>Sensores externos</h3>

        <p>São os responsáveis por detectar as características ambientais e passar
        esses dados ao computador de bordo. Atualmente, os mais utilizados são as
        câmeras, os radares, os sonares e os LIDARs.   
        </p>

        <br/>
      
        <h3>Câmera estereoscópica </h3>

        <p>
        Também chamada de câmera estéreo, é um dispositivo que utiliza
        duas ou mais lentes para criar quadros de diferentes perspectivas.
        Dessa forma, consegue ter noção de profundidade (3D), simulando a visão humana.
        </p>

        <br/>

        <h3>Câmera infravermelha</h3>

        <p>A câmera infravermelha permite uma visualização precisa em ambientes com 
        pouca ou nenhuma luminosidade. Por meio de sensores, esse equipamento é capaz 
        de identificar objetos pela variação de temperatura, captando sua radiação 
        infravermelha, invisível a olho nu.
        </p>

        <br/>

        <h3>Radar</h3>

        <p>Um radar emite ondas de rádio em uma determinada direção, que reverberam 
        nos obstáculos. Ao medir a velocidade e a intensidade desse retorno, consegue 
        ter noções de tamanho e distância.
        </p>

        <br/>

        <h3>Sonar</h3>
        
        <p>O sonar funciona de forma parecida ao radar. A diferença é que, em vez de
          ondas de rádio, usa ondas sonoras, inaudíveis ao ouvido humano.
        </p>

        <h3>LIDAR </h3>

        <p>O LIDAR também segue a lógica dos dois dispositivos anteriores. No entanto, 
        utiliza-se de pulsos de laser, que formam milhares de pontos luminosos, 
        para fazer a varredura do ambiente. Além de ter um sinal mais rápido, o 
        LIDAR permite cobrir uma área mais vasta, em 360°, e com maior precisão.
        </p>

        <br/>

        <h2>Outros componentes</h2>

        <p>Outros componentes também são necessários para possibilitar a
           autonomia de um automóvel. São tecnologias que tornam as ações do computador mais eficientes. 
        </p>

        <br/>

        <h3>ESC (Controle Eletrônico de Estabilidade) </h3>

        <p>O Controle Eletrônico de Estabilidade é a mesma tecnologia usada em diversos 
        modelos, inclusive no Brasil. Nos carros autônomos, é o responsável por 
        calcular e fazer correções na condução de acordo com a velocidade de cada 
        roda, a inclinação e a guinada do veículo.
        </p>

        <br/>

        <h3>iBooster</h3>

        <p>O servofreio eletromecânico a vácuo, chamado iBooster, é capaz de gerar pressão 
        controlada nos freios em menos de 120 milissegundos. Isso é três vezes mais rápido 
        do que os sistemas de frios convencionais, tornando o veículo mais seguro em frenagens 
        de emergência.
        </p>

        <br/>

        <h3>GPS, velocímetro e hodômetro</h3>

        <p>Para que o veículo possa se guiar pelas cidades, é preciso equipá-lo com mapas 
        atualizados e controlar sua localização. Por isso, utilizará equipamentos de GPS 
        integrados ao velocímetro e ao hodômetro. Assim, o computador consegue calcular 
        sua posição mesmo na falta do satélite.
        </p>
        
        <br/>

        <h3>Inteligência Artificial e conectividade</h3>

        <p>A Inteligência Artificial será responsável por captar todos os sinais dos 
        sensores, internos e externos, e controlar a condução, avisar o proprietário 
        sobre a necessidade de manutenção, realizar pequenos reparos e ajustes no sistema, 
        e aprender com falhas, por exemplo. A conectividade com os outros carros autônomos 
        permitirá o compartilhamento de experiências e soluções. 
        </p>

        <br/>

        <h3>O funcionamento dos Carros Autônomos</h3>

        <p>O desenvolvimento dos carros autônomos está em estágio avançado. O objetivo é fazer 
        com que os computadores diferenciem os tipos de obstáculos e de situações nas vias 
        para não só reagir conforme parâmetros preestabelecidos como, também, aprender as 
        eventualidades.
        </p>

        <br/>

        <p>Com a conectividade, os computadores de bordo trocam informações entre si. 
        Assim, um imprevisto ocorrido com um dos automóveis servirá para que todos 
        aprendam a lidar com circunstâncias equivalentes. Para isso, os automóveis 
        precisarão de centrais computadorizadas mais potentes do que os atuais módulos 
        eletrônicos (ECUs) e os sistemas de diagnose OBD (On-Board Diagnostic). 
        </p>

        <br/>

        <p>Na prática, todas as peças do veículo autônomo devem trabalhar juntas para gerar 
        respostas precisas. Além disso eles trabalham com outros cinco níveis de automação 
        que ajudam a entender o estágio em que determinado automóvel se encontra no campo 
        da direção autônoma. Conheça cada um deles:
        </p>

        <br/>

        <ul>
          <li><b>Nível 0</b>: a direção depende 100% do motorista. Nesta categoria estão modelos 
          equipados com sistemas como frenagem autônoma e monitor de pontos cegos ou de 
          mudança involuntária de faixa. Nesta categoria estão modelos como Hyundai HB20 
          e Chevrolet Onix. 
          </li>

          <li><b>Nível 1</b>: o veículo é capaz de fazer algumas intervenções, mas a direção 
          depende do motorista humano. Se enquadram nessa categoria os veículos 
          equipamentos com controlador adaptativo de velocidade de cruzeiro ou 
          assistente de manutenção em faixa. Nesta categoria estão modelos 
          feitos no Brasil como o Volkswagen Nivus e o Jeep Compass.
          </li>

          <li><b>Nível 2</b>: é o padrão no mercado atualmente. Em cenários seguros, o veículo 
          consegue acelerar, frear e se manter dentro de faixas de rodagem. Mas o 
          motorista deve permanecer atento à via todo o tempo para assumir a 
          direção em situações de emergência. A lista de modelos de nível 2 
          inclui o SUV Volvo XC40 e Mercedes-Benz Classe E 
          </li>
          
          <li><b>Nível 3</b>: presente apenas no japonês Honda Legend, é atualmente o mais 
          avançado oferecido em um carro de produção. O sistema autônomo é 
          capaz de guiar sozinho o veículo em congestionamentos. Mas o condutor 
          deve se manter pronto para assumir o controle do veículo quando solicitado.
          </li>

          <li><b>Nível 4</b>: o sistema autônomo de nível 4 assume todas as funções do condutor. 
          Mas em situações adversas, como mudanças climáticas, a máquina pode solicitar 
          que o motorista assuma os comandos.
          </li>

          <li><b>Nível 5</b>: dispensa totalmente os motoristas e os comandos manuais, 
          permitindo dar ordens ao veículo por meio de comando de voz, 
          por exemplo.
          </li>

        </ul>

        <br/>

        <h2>Problemas que esses automóveis podem apresentar</h2>

        <p>Uma das maiores diferenças dos carros autônomos é seu nível de conectividade. 
        Eles estarão o tempo todo ligados à internet. Por conta disso, muito do que 
        fazemos hoje por celulares ou computadores será feito durante um trajeto, 
        por meio do próprio veículo.
        </p>

        <br/>

        <p>Mas essa comodidade tem um custo. Os automóveis se tornarão alvos de ataques virtuais, 
        visando roubar dados ou comprometer seus sistemas. Assim, problemas com vírus e outros 
        programas maliciosos serão mais constantes.
        </p>

        <br/>

        <p>Por outro lado, a condução será mais eficiente, reduzindo a necessidade de manutenções 
        por quilômetro rodado, pois haverá menos freadas bruscas, arrancadas, acelerações 
        desnecessárias e impactos com buracos e obstáculos, bem como haverá menos acidentes. 
        Afinal, sensores e atuadores são mais rápidos e precisos do que humanos.
        </p>

        <br/>

        <p>Porém, o desgaste de peças como amortecedores, pastilhas e discos de freio, pneus, 
        coxins e batentes, e o final da vida útil de itens como filtros e fluidos, praticamente 
        não mudarão — apenas atingirão seu estágio de maior eficiência. 
        Caso os veículos autônomos adotem também a motorização elétrica, todas as partes de injeção 
        de combustível, combustão, arrefecimento e exaustão de gases serão substituídas por sistemas 
        elétricos e eletrônicos. Por conta disso, os carros elétricos precisam de manutenção 
        diferenciada, pois não lidam com explosões e altas temperaturas. 
        </p>

        <br/>

        <h2>Por que eles não chegaram em 2020?</h2>

        <p>Os níveis de automação que mostramos anteriormente já revelam o motivo do atraso. 
        Nos últimos dez anos, empresas desembolsaram muito dinheiro, fizeram parcerias e 
        dedicaram tempo para que a direção autônoma chegasse até o ano de 2020. Apesar disso, 
        como fazer um automóvel que se dirige sozinho ser altamente confiável? Essa é a pergunta 
        que freia a chegada dessa inovação no mundo.
        </p>

        <br/>

        <p>De acordo com a OMS (Organização Mundial da Saúde), anualmente 50 milhões de pessoas se 
        acidentam em rodovias pelo mundo e muitas dessas fatalidades acontecem devido à falha humana. 
        No futuro, os veículos que ganharem autonomia terão de reduzir essa estatística. Sim, é uma 
        responsabilidade muito grande para todas as organizações que trabalham em projetos de carros autônomos. 
        Essas empresas precisam realizar muitos treinamentos com Inteligência Artificial e os carros têm de rodar 
        milhares de quilômetros e fazer viagens longas, sem apresentar nenhum problema de segurança. Já os sensores 
        precisam atuar sem erros lidando com todas as condições climáticas e identificar todos os objetos que podem 
        eventualmente estar na via.
        </p>

        <br/>

        <p>Existe outro obstáculo no caminho: a legislação. No Brasil, além da tradicional burocracia, 
        esbarramos no artigo 252 do Código de Trânsito Brasileiro (CTB), que considera infração média “dirigir 
        o veículo com apenas uma das mãos, exceto quando deva fazer sinais regulamentares de braço, mudar a 
        marcha do veículo, ou acionar equipamentos e acessórios do veículo”.
        </p>

        <br/>

        <p>Por mais que algumas leis entrem em conflito com os carros autônomos, não quer dizer que todos os países 
        e empresas irão entrar em problemas, por exemplo é o que acontece com a Tesla. Há alguns anos, a tesla já 
        vem vendendo carros elétricos que são quase autônomos estando no nível 2 de automação, fazendo um sucesso 
        pelo mundo inteiro. Porem, por mais que a tesla já esteja vendendo seus carros para o mundo, não quer dizer 
        que todos os países terão acesso a eles, esse que no caso é o Brasil. 
        </p>

        <br/>

        <p>O Brasil é o país menos preparado para os carros autônomos, revela o estudo Índice de Prontidão para Veículos 
        Autônomos 2020, da KPMG. Respectivamente, Singapura, Holanda, Noruega, Estados Unidos e Finlândia são os cinco 
        países mais capacitados para receber essa tecnologia. A KPMG lembra no relatório que Singapura investiu mais de 
        US$ 4,3 milhões nesta inovação, permitiu avaliações em suas rodovias e trabalha com metas de adoção massiva. 
        </p>

        <br/>

        <p>Ainda que o Brasil adote novas tecnologias com entusiasmo, o país conflita com várias áreas que impulsionam a 
        direção autônoma e os carros conectados. Além disso, o governo brasileiro não incentiva nem mostra interesse em 
        colocar esses projetos nas ruas. Enquanto isso, apenas empresas do setor privado abraçam os veículos autônomos 
        para uso em suas estruturas. 
        </p>

